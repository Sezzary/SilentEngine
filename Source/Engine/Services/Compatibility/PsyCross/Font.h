int GetTimInfo(ulong* tim, TIM_IMAGE* timimg)
{
    uint* rtim = (uint*)tim;;

    // Check ID.
    if ((rtim[0] & 0xFF) != 0x10)
    {
        return 1;
    }

    // Check version.
    if (((rtim[0] >> 8) & 0xFF) != 0x0)
    {
        return 2;
    }

    timimg->mode = rtim[1];
    rtim        += 2;

    // Clut present?
    if (timimg->mode & 0x8)
    {
        timimg->cRECT16 = (RECT16*)&rtim[1];
        timimg->caddr   = (uint*)&rtim[3];

        rtim += rtim[0] >> 2;
    }
    else
    {
        timimg->caddr = 0;
    }

    timimg->pRECT16 = (RECT16*)&rtim[1];
    timimg->paddr = (uint*)&rtim[3];

    return 0;

}
